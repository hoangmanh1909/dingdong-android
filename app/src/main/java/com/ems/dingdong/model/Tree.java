package com.ems.dingdong.model;

public abstract class Tree {
    private int id;
    private String name;

    abstract boolean isLeaf();

    Tree(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
